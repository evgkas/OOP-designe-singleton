namespace OOP_design_pract_singleton_new
{
    public class Car
    {
        public string brand;
        public string model;
        public int quaintity;
        public double price;

        public Car(string brand, string model, int quaintity, double price)
        {
            this.brand = brand;
            this.model = model;
            this.quaintity = quaintity;
            this.price = price;
        }
    }
}
